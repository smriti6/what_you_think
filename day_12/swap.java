class swap {
    public static void main(String[] args) {
        System.out.println("day 12");
        System.out.println("Swap 2 number");
        int n1=5;
        int n2=10;
        System.out.println("Before swap n1 = "+n1 +"  n2 = "+n2);
        int t=n1;
        n1=n2;
        n2=t;
        System.out.println("After swap n1 = "+n1 +"  n2 = "+n2);
    }
}